float sum(float x,float y)
{
    float z;
    z=x+y;
    return z;
}

float difference(float x, float y)
{
    float z;
    z=x-y;
    return z;
}

float product(float x,float y)
{
    float z;
    z=x*y;
    return z;
}

float quotient(float x,float y)
{
    float z;
    z=x/y;
    return z;
}

float modulo(float x,float y,int v,int w)
{
    v=(x);
    w=(y);
    float z;
    z=v/w;
    return z;
}